#include "libftprintf.h"

long	ft_nbrlen_p(unsigned long n)
{
	long i;

	i = 1;
	while (n >= 16)
	{
		if (n < 10)
			i++;
		if (n >= 16)
		{
			n = n / 16;
			i++;
		}
	}
	return (i);
}
